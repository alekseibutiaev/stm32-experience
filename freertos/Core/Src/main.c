#define ARRAY_SIZE(__ARRAY__) (sizeof(__ARRAY__) / sizeof(*__ARRAY__))

void task_one(void* param) {
  uint32_t buf[32] = { 0 };
  uint32_t* pbuf = buf;
  uint32_t i = 0;
  for(;;) {
    HAL_GPIO_TogglePin(led0_GPIO_Port, led0_Pin);
    vTaskDelay(1000);
    ++buf[i++];
    i = i % ARRAY_SIZE(buf);
  }
  (void)pbuf;
}

void task_two(void* param) {
  for(;;) {
    HAL_GPIO_TogglePin(led1_GPIO_Port, led1_Pin);
    vTaskDelay(500);
  }
}

